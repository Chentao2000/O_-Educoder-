#include <stdio.h>

int main()
{
    int i = 0;
    int j = 0;
    for(;i<10;i++)       //出现for的嵌套语句时，先执行内部循环再执行外部循环
    {
        for(;j<10;j++)    //省略了初始化部分，即(也是)判断条件为真才能执行
        {
            printf("hi");  //最后只会打印十个hi
        }                  //内部执行完后，j=10，当开始进入外部循环再往下进入内部循环时，j<10为假，故结束循环
    }
    return 0;
}
