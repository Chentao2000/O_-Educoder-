#include <stdio.h>

int main()
{
    int a = 0; //4个字节，32位
    int b = ~a; //b是有符号的整型，最高位（最左）就是符号位
    printf("%d\n", b);
    return 0;
}

/* 

正数的原码=反码=补码
负数在内存中存储的时候，存储的是二进制的补码，所以b = 11111111111111111111111111111111 (补码)
但我们打印的时候用的是原码，负数的补码=反码+1，所以负数的原码=补码-1再按位取反
所以得补码-1 = 11111111111111111111111111111110，再按位取反 = 10000000000000000000000000000001即为十进制-1

*/
